public class OA {
    public static void main(String[] args) {
        //operator assignment
        int a = 1;
        a += 5;
        System.out.println("penambahan nilai a = " + a);

        int b = 50;
        b -= 10;
        System.out.println("pengurangan nilai b = " + b);

        int c = 10;
        c *= 5;
        System.out.println("perkalian nilai c = " + c);

        int d = 40;
        d /= 5;
        System.out.println("pembagian nilai d = " + d);

        int e = 12;
        e %= 4;
        System.out.println("modulus nilai e = " + e);
        
    }
}
