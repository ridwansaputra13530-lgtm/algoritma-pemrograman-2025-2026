public class LBD {
    public static void main(String[] args) {
        // luas persegi
        int s1 = 10;
        int s2 = 5;
        int Lpersegi = s1 * s2;
        System.out.println(Lpersegi);
        // luas persegi panjang
        int p = 9;
        int l = 4;
        int LPPanjang = p * l;
        System.out.println(LPPanjang);
        // luas jajar genjang
        int a = 8;
        int t = 6;
        int LjajarGenjang = a * t;
        System.out.println(LjajarGenjang);
        // luas segitiga
        int a1 = 6;
        int t1 = 21;
        double Lsegitiga = 0.5 * a1 * t1;
        System.out.println(Lsegitiga);
        // luas belah ketupat
        int d1 = 10;
        int d2 = 12;
        double LbelahKetupat = 1.0 / 2.0 * d1 * d2;
        System.out.println(LbelahKetupat);
        // luas trapesium
        int sA = 14;
        int sB = 10;
        int t2 = 6;
        double Ltrapesium = 0.5 * (sA + sB) * t2;
        System.out.println(Ltrapesium);
        // luas lingkaran
        int r1 = 7;
        int r2 = 7;
        double LLingkaran = 22.0/7.0 * r1 * r2;
        System.out.println(LLingkaran);
    }
   
}
    

