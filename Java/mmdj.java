public class mmdj {
    public static void main(String[] args) {
        //1. absolute = mutlak
        int nomor = -6;
        System.out.println(Math.abs(nomor));
        //2.pow = pangkat
        int nomor2 = 5;
        System.out.println(Math.pow(nomor2,3));
        //3. sqrt = akar kuadrat
        int Nomor = 64;
        System.out.println(Math.sqrt(Nomor));
        //4. min = minimum
        int angka1 = 2;
        int angka2 = 10;
        System.out.println(Math.min(angka1, angka2));
        //5. max = maksimum
        int Angka1 = 15;
        int Angka2 = 25;
        System.out.println(Math.max(Angka1, Angka2));
        //6. a round = dibulatkan ke bawah
        double ANgka1 = 5.4;
        System.out.println(Math.round(ANgka1));
        //   b round = dibulatkan ke atas
        double ANgka2 = 5.7;
        System.out.println(Math.round(ANgka2));
    }
    
}