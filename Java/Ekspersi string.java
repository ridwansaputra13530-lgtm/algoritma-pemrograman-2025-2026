
public class Ekspresi_String {
    public static void main(String[] args) {
        String kelas = "Reguler pagi";

        System.out.println(kelas.toUpperCase());
        System.out.println(kelas.toLowerCase());
        System.out.println(kelas.length());
        System.out.println(kelas.contains("Pagi"));
        System.out.println(kelas.contains("Hai"));
        System.out.println(kelas.charAt(3));  
        System.out.println(kelas.indexOf( "r"));
        System.out.println(kelas.substring(8));

}
}
