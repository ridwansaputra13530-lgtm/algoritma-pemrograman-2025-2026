a = 80
# disini kita melakukan pengecekan kedua dan ketiga jika pada statemen if bernilai False.
# Kode pada blok ini akan dijalankan hanya jika statemen elif bernilai True.

# Pengecekan pertama dengan if: if(a > 90)
if (a > 90):
    print("Dapat nilai A.") # ini tidak akan dijalankan

# Pengecekan kedua dengan elif: elif(a > 70)
elif (a > 70):
    print("Dapat nilai B.") # ini akan dijalankan